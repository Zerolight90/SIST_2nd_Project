package mybatis.vo;

public class PriceVO {
    private String priceIdx, teen, elder, day, week, morning, normal;

    public String getPriceIdx() {
        return priceIdx;
    }

    public void setPriceIdx(String priceIdx) {
        this.priceIdx = priceIdx;
    }

    public String getTeen() {
        return teen;
    }

    public void setTeen(String teen) {
        this.teen = teen;
    }

    public String getElder() {
        return elder;
    }

    public void setElder(String elder) {
        this.elder = elder;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public String getMorning() {
        return morning;
    }

    public void setMorning(String morning) {
        this.morning = morning;
    }

    public String getNormal() {
        return normal;
    }

    public void setNormal(String normal) {
        this.normal = normal;
    }
}
