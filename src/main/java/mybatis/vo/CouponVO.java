package mybatis.vo;

public class CouponVO {

    private String couponIdx, codeIdx, couponCategory, couponName, couponInfo, couponRegDate, couponExpDate, couponUseDate, couponStatus, discountValue;

    public String getDiscountValue() {
        return discountValue;
    }

    public void setDiscountValue(String discountValue) {
        this.discountValue = discountValue;
    }

    public String getCouponIdx() {
        return couponIdx;
    }

    public void setCouponIdx(String couponIdx) {
        this.couponIdx = couponIdx;
    }

    public String getCodeIdx() {
        return codeIdx;
    }

    public void setCodeIdx(String codeIdx) {
        this.codeIdx = codeIdx;
    }

    public String getCouponCategory() {
        return couponCategory;
    }

    public void setCouponCategory(String couponCategory) {
        this.couponCategory = couponCategory;
    }

    public String getCouponName() {
        return couponName;
    }

    public void setCouponName(String couponName) {
        this.couponName = couponName;
    }

    public String getCouponInfo() {
        return couponInfo;
    }

    public void setCouponInfo(String couponInfo) {
        this.couponInfo = couponInfo;
    }

    public String getCouponRegDate() {
        return couponRegDate;
    }

    public void setCouponRegDate(String couponRegDate) {
        this.couponRegDate = couponRegDate;
    }

    public String getCouponExpDate() {
        return couponExpDate;
    }

    public void setCouponExpDate(String couponExpDate) {
        this.couponExpDate = couponExpDate;
    }

    public String getCouponUseDate() {
        return couponUseDate;
    }

    public void setCouponUseDate(String couponUseDate) {
        this.couponUseDate = couponUseDate;
    }

    public String getCouponStatus() {
        return couponStatus;
    }

    public void setCouponStatus(String couponStatus) {
        this.couponStatus = couponStatus;
    }
}
